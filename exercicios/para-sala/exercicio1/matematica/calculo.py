# Função para somar dois números 'a' e 'b'.
def soma(a, b):
    return a + b # retornar o resultado

# Função para subtrair 'b' de 'a'.
def subtracao(a, b):
    return a - b # retornar o resultado

# Função para multiplicar 'a' por 'b'.
def multiplicacao(a, b):
    return a * b # retornar o resultado

# Função para dividir 'a' por 'b'.
def divisao(a, b):
    return a / b  # retornar o resultado da divisão.
